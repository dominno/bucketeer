export function t(key, params) {
  const loc = (store.getState().locale && DICT[store.getState().locale]) ? store.getState().locale : 'en';
  let str = DICT[loc][key];
  if (str === undefined) str = DICT.en[key];
  if (str === undefined) return key;
  if (params) str = str.replace(/\{(\w+)\}/g, (m, k) => (params[k] != null ? params[k] : m));
  return str;
}

export function getLocale() {
  return store.getState().locale || 'en';
}

export function setLocale(loc) {
  if (!DICT[loc]) return;
  try {
    localStorage.setItem('e2-locale', loc);
  } catch {
    /* ignore */
  }
  document.documentElement.lang = loc;
  store.setState({ locale: loc });
}

export function detectLocale() {
  let saved = null;
  try {
    saved = localStorage.getItem('e2-locale');
  } catch {
    /* ignore */
  }
  if (saved && DICT[saved]) return saved;
  return (navigator.language || 'en').toLowerCase().startsWith('pl') ? 'pl' : 'en';
}

export function initLocale() {
  const loc = detectLocale();
  document.documentElement.lang = loc;
  store.setState({ locale: loc });
}
